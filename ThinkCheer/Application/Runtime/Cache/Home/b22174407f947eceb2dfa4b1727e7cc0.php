						<p>7、蛋浆准备好后，就可以在电饭煲里面抹上一层油，把蛋浆倒进去。盖上盖子，按电饭锅“煮饭”按钮，不知道为什么约5分钟左右就跳到保温了，等了2-3分钟，再按了一次煮饭，几分钟后又跳了，这个时候就可以闻到一股香味，打开盖子，表面是浅黄色的，比原来长高了一点，用筷子戳一下，如果戳得进去，并且筷子没有粘蛋浆，就表示可以了。拿出锅，翻转，就可以弄下来，底部应该有点深色；</p>
